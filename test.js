function sum(a, b) {
  return a + b;
}

function sub(a, b) {
  return a - b;
}

function mul(a, b) {
  return a * b;
}

function div(a, b) {
  return a / b;
}

function main() {
  let a = 1;
  let b = 2;
  console.log(sum(a, b));
  console.log(sub(a, b));
  console.log(mul(a, b));
  console.log(div(a, b));
}

main();
